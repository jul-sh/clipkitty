struct IgnoredAppsListView: View {
    @ObservedObject private var settings = AppSettings.shared
    @State private var ignoredApps: [IgnoredApp] = []
    @State private var selectedAppId: String?

    var body: some View {
        VStack(spacing: 0) {
            // List of ignored apps
            if ignoredApps.isEmpty {
                Text("No applications ignored")
                    .foregroundStyle(.secondary)
                    .frame(maxWidth: .infinity, minHeight: 60)
            } else {
                List(selection: $selectedAppId) {
                    ForEach(ignoredApps) { app in
                        HStack(spacing: 10) {
                            if let icon = app.icon {
                                Image(nsImage: icon)
                                    .resizable()
                                    .frame(width: 24, height: 24)
                            } else {
                                Image(systemName: "app.fill")
                                    .frame(width: 24, height: 24)
                                    .foregroundStyle(.secondary)
                            }
                            Text(app.name)
                            Spacer()
                        }
                        .tag(app.id)
                        .contentShape(Rectangle())
                    }
                }
                .listStyle(.plain)
                .frame(minHeight: 80, maxHeight: 160)
            }

            Divider()

            // Add/Remove buttons
            HStack(spacing: 0) {
                Button(action: addApp) {
                    Image(systemName: "plus")
                        .frame(width: 24, height: 20)
                }
                .buttonStyle(.borderless)

                Divider()
                    .frame(height: 16)

                Button(action: removeSelectedApp) {
                    Image(systemName: "minus")
                        .frame(width: 24, height: 20)
                }
                .buttonStyle(.borderless)
                .disabled(selectedAppId == nil)

                Spacer()
            }
            .padding(.horizontal, 4)
            .padding(.vertical, 4)
        }
        .background(Color(nsColor: .controlBackgroundColor))
        .cornerRadius(6)
        .overlay(
            RoundedRectangle(cornerRadius: 6)
                .stroke(Color(nsColor: .separatorColor), lineWidth: 1)
        )
        .onAppear {
            refreshIgnoredApps()
        }
        .onChange(of: settings.ignoredAppBundleIds) { _, _ in
            refreshIgnoredApps()
        }
    }

    private func refreshIgnoredApps() {
        ignoredApps = settings.ignoredAppBundleIds
            .map { IgnoredApp.fromBundleId($0) }
            .sorted { $0.name.localizedCaseInsensitiveCompare($1.name) == .orderedAscending }
        // Clear selection if selected app was removed
        if let selected = selectedAppId, !ignoredApps.contains(where: { $0.id == selected }) {
            selectedAppId = nil
        }
    }

    private func addApp() {
        let panel = NSOpenPanel()
        panel.title = "Select Application to Ignore"
        panel.canChooseFiles = true
        panel.canChooseDirectories = false
        panel.allowsMultipleSelection = true
        panel.allowedContentTypes = [.application]
        panel.directoryURL = URL(fileURLWithPath: "/Applications")

        // Run modal on main thread to ensure proper display
        let response = panel.runModal()
        guard response == .OK else { return }

        for url in panel.urls {
            if let bundle = Bundle(url: url),
               let bundleId = bundle.bundleIdentifier {
                settings.addIgnoredApp(bundleId: bundleId)
            }
        }
    }

    private func removeSelectedApp() {
        guard let selectedId = selectedAppId else { return }
        settings.removeIgnoredApp(bundleId: selectedId)
        selectedAppId = nil
    }
}

